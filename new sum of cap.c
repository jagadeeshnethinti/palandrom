#include<stdio.h>
int main()
{
	char a[10];
	int i,len,count1=0,count2=0,count3=0;
	scanf("%s",&a);
	len=strlen(a);
	for(i=0;i<len;i++)
	{
		if(isupper(a[i]))
		{
			count1++;
	    } 
		if(islower(a[i]))
		{
			count2++;
		}
		if(isdigit(a[i]))
		{
			count3++;
		}
	
	}
	printf(" uppercase letter:%d\n",count1);
	printf("lower case number are:%d\n",count2);
		printf(" digits are:%d",count3);
}
